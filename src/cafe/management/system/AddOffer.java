    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        Offer o= new Offer();
         o.setId(txtofferid.getText());
        o.setPercent(txtdiscount.getText());
        o.setStatus((String)txtstatus.getSelectedItem());
        
        OfferDao.save(o);
        JOptionPane.showMessageDialog(null,"Offer Added Successfully!","Success",JOptionPane.INFORMATION_MESSAGE);
       
        txtofferid.setText("");
        txtdiscount.setText("");
        DefaultTableModel dtmS = (DefaultTableModel) toffer.getModel();
        ArrayList<Offer> itemS = OfferDao.getAllOffers();
        Iterator<Offer> itrS = itemS.iterator();
        int j=dtmS.getRowCount();
        for(int i=0;i<j;i++){
          dtmS.removeRow(0);
        }
         while(itrS.hasNext()){
            Offer oe = itrS.next();
            dtmS.addRow(new Object[]{oe.getId(),oe.getPercent(),oe.getStatus()});
         }  
        
         
    }//GEN-LAST:event_btnsaveActionPerformed

