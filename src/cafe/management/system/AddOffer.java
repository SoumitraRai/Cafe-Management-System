    private void tofferMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tofferMouseClicked
        // TODO add your handling code here:
        DefaultTableModel dtmS = (DefaultTableModel) toffer.getModel();
        int row=toffer.rowAtPoint(evt.getPoint());
        int col=toffer.columnAtPoint(evt.getPoint());
        String idrow=(String) dtmS.getValueAt(row,0);
        if(col==2)
        {
            String curr=(String) dtmS.getValueAt(row, col);
            String newv=curr.equals("False")?"True":"False";
           
            dtmS.setValueAt(newv, row, col);
            OfferDao.updatestatus(idrow,newv);
        }
        
    }//GEN-LAST:event_tofferMouseClicked

