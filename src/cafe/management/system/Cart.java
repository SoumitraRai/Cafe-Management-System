    private Coupon getcupponbyname(String name){
        Coupon c=new Coupon();
        
        Iterator<Coupon> itr = cuppons.iterator();
        while(itr.hasNext()){
            Coupon lc = itr.next();
            if(lc.getId().equals(name)){
                return lc;
            }
        }
        
        JOptionPane.showMessageDialog(null, "wrong cuppon entered");
        return c;
        
    }
