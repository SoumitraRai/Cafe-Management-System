     private void setSum(){
         DefaultTableModel dtm=(DefaultTableModel) tCart.getModel();
         int rc=dtm.getRowCount();
         int sum=0;
         for(int i=0;i<rc;i++){
             int s = (Integer) dtm.getValueAt(i, 4);
             sum+=s;
             
             
         }
         txtSum.setText(String.valueOf(sum));
     }
     
     private void setTable(){
         DefaultTableModel dtm = (DefaultTableModel) tCart.getModel();
          Iterator<UserCart> itr = usercart.iterator();
        int i=1;
        int cr=dtm.getRowCount();
        for(int j=0;j<cr;j++){
            dtm.removeRow(0);
        }
        while(itr.hasNext()){
            
            UserCart c =  itr.next();
            //
            System.out.println(c.getItem().getName());
            if(!c.getQty().equals("0")){
                 dtm.addRow(new Object[]{i,c.getItem().getName(),c.getQty(),c.getItem().getPrice(),Integer.parseInt(c.getItem().getPrice())*Integer.parseInt(c.getQty())});
                 i++;
            }else{
                itr.remove();
            }
           
            
        }
        setSum();
         
     }
     private void setoffer(){
        
        //
//         txtSum.setText("1000");
        //
        String ot =   txtSum.getText();
        
        
        Double sum=Double.parseDouble(txtSum.getText());
        Double per=Double.parseDouble(offer.getPercent());
        
        if(!offer.getId().equals("") && sum>100){
            
            
         sum=sum-(sum*per)/100;
            
         txtSum.setText(String.valueOf(sum));
         lOfferapplied.setVisible(true);
         lOfferapplied.setText("Offer applied !!");
         lOfferid.setText("offer id:"+offer.getId());
         lOfferid1.setText("old total :"+ot);
         lOfferid2.setText("discout : "+offer.getPercent()+"%");
        
        
        }else{
            lOfferapplied.setText("");
            lOfferid.setText("");
            lOfferid1.setText("");
            lOfferid2.setText("");
                    
        }
        if(sum<150){
            btnapply.setEnabled(false);
        }else{
            btnapply.setEnabled(true);
        }
       
        txtCuppon.setEditable(true);
         txtCuppon.setText("");
     }
     

