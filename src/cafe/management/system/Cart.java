    private void tCartKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tCartKeyPressed
        // TODO add your handling code here:
        int ind = tCart.getSelectedRow();
        TableModel tm = tCart.getModel();
        
        String q = tm.getValueAt(ind,2).toString();
        
        int qt=Integer.parseInt(q);
        if(qt<=0){
            q="0";
        }
        
        
        UserCart nu = usercart.get(ind);
        nu.setQty(q);
        
        usercart.set(ind,nu );
        setTable();
        
        setoffer();
        
    }//GEN-LAST:event_tCartKeyPressed

